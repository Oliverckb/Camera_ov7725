#include "IC.h"


/*******************************************************
 函数名：Init_IC0()
 参数：无
       
 功能：初始化IOC0为输入捕捉,PT0下降沿触发（场同步)
*******************************************************/
void Init_IC0(void)   //
{ 
   TIOS=0x00;    //0-7 开放输入捕捉功能          
   TCTL3=0x00;  
   TCTL4=0x02;   //PT0下降沿沿触发（场同步）  
   TSCR1=0x80;   //定时器使能     
   TIE=0x01;     //输入捕捉/输出比较通道0中断使能   
   TFLG1=0xFF;   //中断标志位写1清零      
} 