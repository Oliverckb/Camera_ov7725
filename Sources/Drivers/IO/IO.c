#include "IO.h"


/*******************************************************
 函数名IInit_PORTA()
 参数：config   0--输入
                1--输出
       
 功能：初始化PORTA
*******************************************************/
void Init_PORTA(unsigned char config)
{
    if(config==0)
    {
        DDRA=0x00; 
    }
    else
    {
        DDRA=0xff;
    }
}
/*******************************************************
 函数名：Set_PORTA()
 参数：config   0--PORTA=00000000
                1--PORTA=11111111
       
 功能：设置PORTA输出高低电平
*******************************************************/
void Set_PORTA(uchar config)
{
    if(config==0)
    {
        PORTA=0x00; 
    }
    else
    {
        PORTA=0xff;
    }
}
/*******************************************************
 函数名：Read_PORTA()
 参数：无
 返回值:PORTA的数据     
 功能：读取PORTA电平
*******************************************************/
uchar Read_PORTA(void)
{
    return PORTA;
}



